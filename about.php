    WOLBENO Limited is a leading Services company open to a wide range of engineering works and has its headquarters in Port Harcourt (NO. 12 Eliozu Road, Eliozu, Port Harcourt, Rivers State) with branch offices in Enugu, and Lagos states.
WOLBENO Limited is registered with the Nigerian Corporate Affairs Commission (C.A.C) as a Private company with Reg. No: RC 1107125. The services of which Wolbeno Ltd has been structured and charged to execute are:
Oil and gas services, independent petroleum marketing, sale and supplies of petroleum chemicals and products, prospecting and mining of solid minerals, erection of structures for the purpose of distribution and storage of petroleum products  viz: D.P.K, A.G.O and P.M.S respectively.
 Maritime services, offshore services, dredging, shipping, equipment lifting, haulage, procurement, Logistics, clearing and forwarding.
Building and maintenance of Estates and Resorts, sales and lease of construction materials, civil construction works, electrical/ mechanical works, renovation, technical services etc.
Manufacturing business/ manufacturer’s representatives, warehousing, marketing services, import and export, agriculture, general Contracts/ Supplies.
And other business activities directly or indirectly connected to the aforementioned services. 
The company was established in 2003 by young Goal oriented people from all walks of Life came together with a vision to render international services within and beyond the engineering field to the Nigerian nation and the world in general.
We have qualified and dedicated young professionals from all fields of engineering who are Project Managers, Project Supervisor and Foremen. We also have Geographers, Architects, Surveyors, Welders, Craftsmen, Security officers, Technicians, Medical doctors, Machine operators, Linguists, Cleaners and Cooks etc.
Our Excellent relationship with other companies and multi-nationals grants us easy access to multi-tasking jobs and general contracts which we perfectly execute completely against all odds. Our Dynamic work force succeeds by the aid of a highly nurtured and purpose driven Team Spirit. 
